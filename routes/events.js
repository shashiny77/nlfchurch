const express = require("express");

const router = express.Router();

module.exports = router;

const Model = require("../model/eventModel");

//Post Method - Stories
router.post("/events", async (req, res) => {
  const data = new Model({
    dateOfEvent: req.body.dateOfEvent,
    placeOfEvent: req.body.placeOfEvent,
    timeOfEvent: req.body.timeOfEvent,
    recurringEvent: req.body.recurringEvent,
    nameOfEvent: req.body.nameOfEvent,
  });

  try {
    const dataToSave = await data.save();
    res.status(200).json(dataToSave);
  } catch (error) {
    res.status(400).json({ message: error.message });
  }
});

//Get all Method - Stories
router.get("/events", async (req, res) => {
  try {
    const data = await Model.find();
    res.json(data);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

//Get by ID Method - Stories
router.get("/events/:id", async (req, res) => {
  try {
    const data = await Model.findById(req.params.id);
    res.json(data);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

//Update by ID Method - Stories
router.patch("/events/:id", async (req, res) => {
  try {
    const id = req.params.id;
    const updatedData = req.body;
    const options = { new: true };

    const result = await Model.findByIdAndUpdate(id, updatedData, options);

    res.send(result);
  } catch (error) {
    res.status(400).json({ message: error.message });
  }
});

//Delete by ID Method - Stories
router.delete("/events/:id", async (req, res) => {
  try {
    const id = req.params.id;
    const data = await Model.findByIdAndDelete(id);
    res.send(`Document with ${data.name} has been deleted..`);
  } catch (error) {
    res.status(400).json({ message: error.message });
  }
});
